str1 = "owais"
print(str1.capitalize())     # Capitalizes first letter of the string

str2 = "owais4"
print(str2.isalnum())   # True

str3 = "owais4"
print(str3.isalpha())   # False

str4 = "1234"
print(str4.isdigit())   # True

str5 = "OWAIS"
print(str5.isupper())   # True

str6 = "owais"
print(len(str6))    # 5

str7 = "owais"
print(str7.upper())     # OWAIS

str8 = "OWAIS"
print(str8.lower())     # owais

str9 = "   hello   "
print(str9.strip())     # Removes all leading and trailing whitespaces.

str10 = "   hello"
print(str10.lstrip())     # Removes all leading(left) whitespaces.

str11 = "python"
print(max(str11))     # Returns the highest alphabetical character having highest ASCII value.

str12 = "flutter"
print(str12.replace("tt", "zz"))     # Removes all leading and trailing whitespaces.

