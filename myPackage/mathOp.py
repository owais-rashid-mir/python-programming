def addFxn(a, b):
    return a + b


def subFxn(a, b):
    return a - b


def mulFxn(a, b):
    return a * b


def divFxn(a, b):
    return a / b


def modFxn(a, b):
    return a % b


def quotientFxn(a, b):
    return a // b


def averageFxn(x, y):
    return (x+y)/2


def powerFxn(x, y):
    return x ** y
