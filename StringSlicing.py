#Slicing is used to extraxt subsets of strings by using the Slice operator- [] [:]
# Index of 1st character is 0 and index of last character is -1

str = "Python is easy"

print(str)      # Python is easy
print(str[0])   # P
print(str[3:9])     # hon is
print(str[4:])      # on is easy
print(str[-1])      # y     - Prints the last character of string
print(str[:5])      # Pytho